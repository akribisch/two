package com.web.entity;

import java.util.Date;

/**
 * 贷款表实体类
 * @author Administrator
 *
 */
public class Credit {
	/**
	 * 贷款编号
	 */
	private int creditId;
	/**
	 * 储蓄卡
	 */
	private Savings savings;
	/**
	 * 贷款金额
	 */
	private double money;
	/**
	 * 贷款时间
	 */
	private Date currentTime;
	/**
	 * 总贷款周期
	 */
	private Date totalTime;
	/**
	 * 贷款银行
	 */
	private Bank bank;
	/**
	 * 办理员工
	 */
	private Staff staff;
	/**
	 * 贷款利率
	 */
	private Interest interest;
	/**
	 * 剩余欠款金额
	 */
	private double surplusMoney;
	
	public Credit() {
		super();
	}
	
	public Credit(int creditId, Savings savings, double money, Date currentTime, Date totalTime, Bank bank, Staff staff,
			Interest interest, double surplusMoney) {
		super();
		this.creditId = creditId;
		this.savings = savings;
		this.money = money;
		this.currentTime = currentTime;
		this.totalTime = totalTime;
		this.bank = bank;
		this.staff = staff;
		this.interest = interest;
		this.surplusMoney = surplusMoney;
	}
	/**
	 * @return the creditId
	 */
	public int getCreditId() {
		return creditId;
	}
	/**
	 * @param creditId the creditId to set
	 */
	public void setCreditId(int creditId) {
		this.creditId = creditId;
	}
	/**
	 * @return the savings
	 */
	public Savings getSavings() {
		return savings;
	}
	/**
	 * @param savings the savings to set
	 */
	public void setSavings(Savings savings) {
		this.savings = savings;
	}
	/**
	 * @return the money
	 */
	public double getMoney() {
		return money;
	}
	/**
	 * @param money the money to set
	 */
	public void setMoney(double money) {
		this.money = money;
	}
	/**
	 * @return the currentTime
	 */
	public Date getCurrentTime() {
		return currentTime;
	}
	/**
	 * @param currentTime the currentTime to set
	 */
	public void setCurrentTime(Date currentTime) {
		this.currentTime = currentTime;
	}
	/**
	 * @return the totalTime
	 */
	public Date getTotalTime() {
		return totalTime;
	}
	/**
	 * @param totalTime the totalTime to set
	 */
	public void setTotalTime(Date totalTime) {
		this.totalTime = totalTime;
	}
	/**
	 * @return the bank
	 */
	public Bank getBank() {
		return bank;
	}
	/**
	 * @param bank the bank to set
	 */
	public void setBank(Bank bank) {
		this.bank = bank;
	}
	/**
	 * @return the staff
	 */
	public Staff getStaff() {
		return staff;
	}
	/**
	 * @param staff the staff to set
	 */
	public void setStaff(Staff staff) {
		this.staff = staff;
	}
	/**
	 * @return the interest
	 */
	public Interest getInterest() {
		return interest;
	}
	/**
	 * @param interest the interest to set
	 */
	public void setInterest(Interest interest) {
		this.interest = interest;
	}
	/**
	 * @return the surplusMoney
	 */
	public double getSurplusMoney() {
		return surplusMoney;
	}
	/**
	 * @param surplusMoney the surplusMoney to set
	 */
	public void setSurplusMoney(double surplusMoney) {
		this.surplusMoney = surplusMoney;
	}
	
}
