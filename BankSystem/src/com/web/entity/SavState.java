package com.web.entity;
/**
 * 账号状态实体类
 * @author Administrator
 *
 */
public class SavState {
	/**
	 * 账号状态编号
	 */
	private int savId;
	/**
	 * 状态说明
	 */
	private String savStat;
	public int getSavId() {
		return savId;
	}
	public void setSavId(int savId) {
		this.savId = savId;
	}
	public String getSavStat() {
		return savStat;
	}
	public void setSavStat(String savStat) {
		this.savStat = savStat;
	}
	public SavState(int savId, String savStat) {
		super();
		this.savId = savId;
		this.savStat = savStat;
	}
	public SavState() {
		super();
	}
	
}
