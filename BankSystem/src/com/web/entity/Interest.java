package com.web.entity;

/**
 * 利率表实体类
 * @author Administrator
 *
 */
public class Interest {
	/**
	 * 利率编号
	 */
	private int interestId;
	/**
	 * 利率种类
	 */
	private String interestName;
	/**
	 * 利率值
	 */
	private double interest;
	/**
	 * 计息周期
	 */
	private int interestCycle;
	
	public Interest() {
		super();
	}
	
	public Interest(int interestId, String interestName, double interest, int interestCycle) {
		super();
		this.interestId = interestId;
		this.interestName = interestName;
		this.interest = interest;
		this.interestCycle = interestCycle;
	}
	/**
	 * @return the interestId
	 */
	public int getInterestId() {
		return interestId;
	}
	/**
	 * @param interestId the interestId to set
	 */
	public void setInterestId(int interestId) {
		this.interestId = interestId;
	}
	/**
	 * @return the interestName
	 */
	public String getInterestName() {
		return interestName;
	}
	/**
	 * @param interestName the interestName to set
	 */
	public void setInterestName(String interestName) {
		this.interestName = interestName;
	}
	/**
	 * @return the interest
	 */
	public double getInterest() {
		return interest;
	}
	/**
	 * @param interest the interest to set
	 */
	public void setInterest(double interest) {
		this.interest = interest;
	}
	/**
	 * @return the interestCycle
	 */
	public int getInterestCycle() {
		return interestCycle;
	}
	/**
	 * @param interestCycle the interestCycle to set
	 */
	public void setInterestCycle(int interestCycle) {
		this.interestCycle = interestCycle;
	}
	
}
