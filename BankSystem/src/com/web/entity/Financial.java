package com.web.entity;
/**
 * 金融理财
 * @author akri
 *
 */
public class Financial {

	/**
	 * 产品编号
	 */
	private int finId;
	
	/**
	 * 产品名称
	 */
	private String finName;
	
	/**
	 * 产品类型
	 */
	private FinalKind finalKind;

	public int getFinId() {
		return finId;
	}

	public void setFinId(int finId) {
		this.finId = finId;
	}

	public String getFinName() {
		return finName;
	}

	public void setFinName(String finName) {
		this.finName = finName;
	}

	public FinalKind getFinalKind() {
		return finalKind;
	}

	public void setFinalKind(FinalKind finalKind) {
		this.finalKind = finalKind;
	}
	
}
